Dynamic web content is website content that is generated during page loading from various sources. The sources can include:

<ul>
  <li>Session Variables</li>
  <li>Database lookups</li>
  <li>Cookies</li>
</ul>

Dynamic web content is opposed to static content that is hard coded into HTML pages. When you have dynamic content from a dynamic language you can have a much more flexible website that tailors content to users.

In order to use dynamic content you need to use a dynamic language (e.g. PHP, javascript) that can fetch and load content depending on different criteria.
